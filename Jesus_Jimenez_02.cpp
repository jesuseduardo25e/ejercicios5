#include <stdio.h>

int main() {
    int n = 50; // Número de elementos en la lista
    int lista[n]; // Vector para almacenar los elementos
    int suma = 0; // Variable para la suma

    // Llenar el vector con los elementos (puedes hacerlo manualmente o de manera aleatoria)
    // Por ejemplo, aquí lo llenamos con valores fijos:
    for (int i = 0; i < n; i++) {
        printf("Ingrese el elemento %d: ", i + 1);
        scanf("%d", &lista[i]);
    }

    // Calcular la suma de los elementos
    for (int i = 0; i < n; i++) {
        suma += lista[i];
    }

    printf("La suma de los %d elementos es: %d\n", n, suma);

    return 0;
}
